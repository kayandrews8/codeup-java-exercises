public class StringExercise {
    public static void main(String[] args) {
        String educated = "We don't need no education \nWe don't need no thought control";
        System.out.println(educated);

        String checkThisOut = "Check this out!, \"s inside of \"s!";
        System.out.println(checkThisOut);

        String windows = "In windows, the main drive is usually C:\\";
        System.out.println(windows);

        String backslashes = "I can do backslashes \\, double backslashes \\\\, and the amazing triple backslash \\\\\\!";
        System.out.println(backslashes);
    }
}
